import groovy.sql.Sql

def url = 'jdbc:hsqldb:gappDB'
def user = 'sa'
def password = ''
def driver = 'org.hsqldb.jdbcDriver'
def sql = Sql.newInstance(url, user, password, driver)

// ... create 'sql' instance
sql.execute '''
  CREATE TABLE TB_Users (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY,
    user_name   VARCHAR(64),
    user_password    VARCHAR(64),
    version     INTEGER
  );
  CREATE TABLE TB_Roles (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY,
    id_user     INTEGER,
    name        VARCHAR(64),
    version     INTEGER
  );
  CREATE TABLE TB_Currency (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY,
    name        VARCHAR(64),
    code        VARCHAR(3),
    version     INTEGER
  );
  CREATE TABLE TB_Bank (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY,
    name        VARCHAR(64),
    version     INTEGER
  );
  CREATE TABLE TB_Trade (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY,
    type        VARCHAR(10),
    currency    VARCHAR(3),
    amount      DECIMAL(10,2),
    rateType    VARCHAR(5),
    version     INTEGER
  );
'''
// close 'sql' instance ...

sql.close()